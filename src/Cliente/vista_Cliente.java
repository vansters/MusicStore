    private void btn_BorrarMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_BorrarMousePressed
        btn_Borrar.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Recursos/delete-push.png")));
        System.out.println("Valor de Index_Carrito en Borrar "+index_Carrito);
        if (index_Carrito != -1 ) {
            Cuenta operacion = (Cuenta) Cuenta.get(index_Carrito);
            System.out.println("El precio del Producto a Remover es "+ operacion.precioSong+" "+operacion.nombreSong);
            total = total - operacion.precioSong;
            label_Total.setText("Total a Pagar : "+ total+"  $");
            btn_Borrar.setEnabled(false);
            Cuenta.remove(index_Carrito);
            modeloLista.removeElementAt(index_Carrito);
            System.out.println("Numero de Ventas(Borrar) = "+Cuenta.size());
        }else {
            System.out.println("El valor de Index es Negativo");
        }
        btn_Borrar.setEnabled(false);
    }//GEN-LAST:event_btn_BorrarMousePressed

